def load_gsm8k_standard(n: int = N_STANDARD) -> list[dict[str, Any]]:
    """Load the first `n` questions from the GSM8K test split.

    **Detailed explanation for engineers:**
        Uses HuggingFace `datasets` library. We take the first `n` questions
        for reproducibility — the same subset is always used, matching the
        ordering in Exp 181's full run. CARNOT_FORCE_LIVE=1 means no synthetic
        fallback; if the dataset cannot be loaded, we exit.

    Returns:
        List of dicts: {question, ground_truth (int), answer_text, source, idx}
    """
    try:
        from datasets import load_dataset
    except ImportError:
        print("FATAL: `datasets` library not installed. Install: pip install datasets")
        sys.exit(1)

    print(f"  Loading GSM8K test split (first {n} questions)...")
    ds = None
    for dataset_name in ("openai/gsm8k", "gsm8k"):
        try:
            ds = load_dataset(dataset_name, "main", split="test")
            print(f"  Loaded {len(ds)} examples from '{dataset_name}'.")
            break
        except Exception as e:
            print(f"  {dataset_name} failed: {e}")

    if ds is None:
        print("FATAL: Could not load GSM8K. Cannot proceed without real data.")
        sys.exit(1)

    questions: list[dict[str, Any]] = []
    for idx in range(len(ds)):
        ex = ds[idx]
        gt = _extract_gsm8k_answer(ex["answer"])
        if gt is None:
            continue
        questions.append({
            "question": ex["question"],
            "ground_truth": gt,
            "answer_text": ex["answer"],
            "source": "gsm8k_real",
            "idx": idx,
        })
        if len(questions) >= n:
            break

    print(f"  Using {len(questions)} standard questions.")
    return questions


def _extract_gsm8k_answer(answer_text: str) -> int | None:
    """Extract the final numeric answer from a GSM8K answer string (####)."""
    match = re.search(r"####\s*(-?[\d,]+)", answer_text)
    if match:
        try:
            return int(match.group(1).replace(",", ""))
        except ValueError:
            return None
    return None


