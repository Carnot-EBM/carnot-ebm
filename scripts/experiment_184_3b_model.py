# 12. Run benchmark on one split (standard or adversarial).
# ---------------------------------------------------------------------------


def run_split(
    split_name: str,
    questions: list[dict[str, Any]],
    model: Any,
    tokenizer: Any,
    q_text_key: str,
    ckpt_interval: int = 50,
) -> dict[str, Any]:
    """Run baseline + verify-repair benchmark on a list of questions.

    **Detailed explanation for engineers:**
        Runs both modes for every question in order. Checkpoints every
        ckpt_interval questions. Prints running accuracy every 10 questions.
        Returns a dict with per-question results and aggregate statistics.

    Args:
        split_name: Short name for checkpointing and logging (e.g. "standard").
        questions: List of question dicts (must have q_text_key + 'ground_truth').
        model: Loaded model.
        tokenizer: Matching tokenizer.
        q_text_key: Dict key that holds the question text.
        ckpt_interval: How often to write a checkpoint (in questions).

    Returns:
        Dict with 'baseline', 'verify_repair' result lists and 'statistics'.
    """
    n = len(questions)
    print(f"\n  [{split_name}] Running {n} questions × 2 modes "
          f"(baseline + verify-repair)...")

    # Load checkpoint if available.
    ckpt = load_checkpoint(split_name)
    if ckpt is not None:
        completed = min(len(ckpt.get("baseline", [])), len(ckpt.get("verify_repair", [])))
        print(f"  Resuming from checkpoint: {completed}/{n} done.")
    else:
        ckpt = {"baseline": [], "verify_repair": []}
        completed = 0

    mode_results = ckpt
    split_t0 = time.time()
    last_ckpt_save = completed

    for qi in range(completed, n):
        q = questions[qi]

        # Mode 1: Baseline.
        r_base = run_baseline(q, model, tokenizer, q_text_key=q_text_key)
        mode_results["baseline"].append(r_base)

        # Mode 2: Verify+Repair.
        r_repair = run_verify_repair(q, model, tokenizer, q_text_key=q_text_key)
        mode_results["verify_repair"].append(r_repair)

        # Running progress every 10 questions.
        done = qi + 1
        if done % 10 == 0 or done == n:
            n_b = sum(1 for r in mode_results["baseline"] if r["correct"])
            n_r = sum(1 for r in mode_results["verify_repair"] if r["correct"])
            total_done = len(mode_results["baseline"])
            elapsed_m = (time.time() - split_t0) / 60
            eta_m = (elapsed_m / max(done - completed, 1)) * (n - done)
            print(
                f"  [{split_name}] {total_done}/{n} | "
                f"baseline {n_b/total_done:.1%} | "
                f"repair {n_r/total_done:.1%} | "
                f"{elapsed_m:.1f}min elapsed | ETA {eta_m:.0f}min"
            )

        # Checkpoint every ckpt_interval questions.
        if done - last_ckpt_save >= ckpt_interval:
            save_checkpoint(split_name, mode_results)
            last_ckpt_save = done

    split_elapsed = time.time() - split_t0

    # Final checkpoint.
    save_checkpoint(split_name, mode_results)

    # Compute bootstrap statistics.
    print(f"  Computing bootstrap CIs (n={N_BOOTSTRAP:,} samples)...")
    base_flags = [r["correct"] for r in mode_results["baseline"]]
    repair_flags = [r["correct"] for r in mode_results["verify_repair"]]

    base_acc, base_lo, base_hi = bootstrap_ci(base_flags, seed=BOOTSTRAP_SEED_BASE + hash(split_name) % 1000)
    repair_acc, repair_lo, repair_hi = bootstrap_ci(repair_flags, seed=BOOTSTRAP_SEED_BASE + hash(split_name) % 1000 + 1)
    delta, delta_lo, delta_hi = bootstrap_delta_ci(base_flags, repair_flags, seed=BOOTSTRAP_SEED_BASE + hash(split_name) % 1000 + 2)

    n_repaired = sum(1 for r in mode_results["verify_repair"] if r.get("repaired", False))
    avg_repairs = sum(r.get("n_repairs", 0) for r in mode_results["verify_repair"]) / n if n > 0 else 0.0

    stats = {
        "n_questions": n,
        "split": split_name,
        "run_time_s": round(split_elapsed, 1),
        "baseline": {
            "accuracy": round(base_acc, 6),
            "ci_lower": round(base_lo, 6),
            "ci_upper": round(base_hi, 6),
            "n_correct": int(sum(base_flags)),
        },
        "verify_repair": {
            "accuracy": round(repair_acc, 6),
            "ci_lower": round(repair_lo, 6),
            "ci_upper": round(repair_hi, 6),
            "n_correct": int(sum(repair_flags)),
        },
        "improvement_delta": round(delta, 6),
        "ci_delta_lower": round(delta_lo, 6),
        "ci_delta_upper": round(delta_hi, 6),
        "n_repaired": n_repaired,
        "avg_repairs_per_question": round(avg_repairs, 4),
    }

    # Print split summary.
    sep = "-" * 60
    print(f"\n  {sep}")
    print(f"  [{split_name.upper()}] RESULTS ({split_elapsed:.1f}s = {split_elapsed/60:.1f}min)")
    print(f"    Baseline:      {int(sum(base_flags))}/{n} "
          f"({base_acc:.1%} [{base_lo:.1%}, {base_hi:.1%}])")
    print(f"    Verify+Repair: {int(sum(repair_flags))}/{n} "
          f"({repair_acc:.1%} [{repair_lo:.1%}, {repair_hi:.1%}])")
    print(f"    Δ (repair-baseline): {delta:+.1%} [{delta_lo:+.1%}, {delta_hi:+.1%}]")
    print(f"    Questions repaired: {n_repaired} ({n_repaired/n:.1%})")
    if delta_lo > 0:
        print(f"    CI EXCLUDES ZERO → verify-repair improves accuracy (p<0.05 approx).")
    else:
        print(f"    CI INCLUDES ZERO → improvement not significant at this N.")
    print(f"  {sep}")

    return {
        "per_question": mode_results,
        "statistics": stats,
    }


