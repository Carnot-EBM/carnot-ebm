def load_3b_model() -> tuple[Any, Any, dict[str, Any]]:
    """Try each 3B model candidate in order, return first that loads.

    **Detailed explanation for engineers:**
        Uses device_map={"": DEVICE_INDEX} (identified as the correct multi-GPU
        placement in Exp 180) so all layers are placed on GPU 0 at load time.
        float16 halves memory vs float32: 3B params × 2 bytes ≈ 6 GB VRAM.

        Returns:
            (tokenizer, model, metadata_dict) where metadata_dict records
            which model ID was actually loaded, load time, and VRAM used.

        Raises:
            SystemExit: If no candidate model can be loaded (CARNOT_FORCE_LIVE=1).
    """
    vram_before = torch.cuda.memory_allocated(DEVICE_INDEX) / 1024 ** 2
    print(f"  GPU {DEVICE_INDEX} VRAM before load: {vram_before:.0f} MB")

    for candidate in MODEL_CANDIDATES:
        hf_id = candidate["hf_id"]
        dtype = torch.float16 if candidate["dtype"] == "float16" else torch.float32

        print(f"  Trying {hf_id} on {DEVICE_STR} (float16)...")
        t_load = time.perf_counter()
        try:
            tokenizer = AutoTokenizer.from_pretrained(
                hf_id, trust_remote_code=True,
            )
            model = AutoModelForCausalLM.from_pretrained(
                hf_id,
                trust_remote_code=True,
                torch_dtype=dtype,
                device_map={"": DEVICE_INDEX},
            )
            model.eval()
            load_time = time.perf_counter() - t_load
            vram_after = torch.cuda.memory_allocated(DEVICE_INDEX) / 1024 ** 2
            vram_delta = vram_after - vram_before

            print(f"  Loaded {hf_id} in {load_time:.2f}s | "
                  f"VRAM: {vram_before:.0f} → {vram_after:.0f} MB (+{vram_delta:.0f} MB)")

            # Smoke test with a trivial arithmetic prompt.
            smoke = _generate(model, tokenizer, "What is 2 + 2?", 20)
            print(f"  Smoke test OK: '{smoke[:40].strip()}'")

            return tokenizer, model, {
                "name": candidate["name"],
                "hf_id": hf_id,
                "device": DEVICE_STR,
                "device_index": DEVICE_INDEX,
                "load_time_s": round(load_time, 3),
                "vram_before_mb": round(vram_before, 1),
                "vram_after_mb": round(vram_after, 1),
                "vram_delta_mb": round(vram_delta, 1),
                "dtype": "float16",
                "live": True,
            }

        except Exception as exc:
            print(f"  FAILED to load {hf_id}: {exc}")
            gc.collect()
            torch.cuda.empty_cache()
            continue

    print("FATAL: No 3B model candidate could be loaded.")
    print("  Candidates tried:", [c["hf_id"] for c in MODEL_CANDIDATES])
    sys.exit(1)


def unload_model(model: Any, tokenizer: Any) -> None:
    """Free GPU VRAM after use.

    **Detailed explanation for engineers:**
        Deletes Python references and calls torch.cuda.empty_cache() to return
        cached VRAM to the OS pool. gc.collect() clears cyclic refs.
    """
    del model, tokenizer
    gc.collect()
    torch.cuda.empty_cache()
    vram = torch.cuda.memory_allocated(DEVICE_INDEX) / 1024 ** 2
    print(f"  Unloaded model. GPU{DEVICE_INDEX} VRAM now: {vram:.0f} MB")


# ---------------------------------------------------------------------------
# 2. Text generation helper (identical to Exp 181 for comparability).
# ---------------------------------------------------------------------------


def _generate(
    model: Any,
    tokenizer: Any,
    prompt: str,
    max_new_tokens: int,
) -> str:
    """Generate text on DEVICE_STR; strip Qwen3 thinking tokens.

    **Detailed explanation for engineers:**
        Applies the HuggingFace chat template if available (Qwen3.5/3 use it).
        enable_thinking=False suppresses Qwen3's internal chain-of-thought
        preamble; we catch TypeError for tokenizers that don't support it.
        Greedy decoding (do_sample=False) ensures reproducibility.
        Strips <think>…</think> sections that some Qwen3 checkpoints emit.
    """
    messages = [{"role": "user", "content": prompt}]
    try:
        text = tokenizer.apply_chat_template(
            messages, tokenize=False, add_generation_prompt=True,
            enable_thinking=False,
        )
    except TypeError:
        try:
            text = tokenizer.apply_chat_template(
                messages, tokenize=False, add_generation_prompt=True,
            )
        except Exception:
            text = prompt
    except Exception:
        text = prompt

    inputs = tokenizer(text, return_tensors="pt")
    inputs = {k: v.to(DEVICE_STR) for k, v in inputs.items()}
    input_len = inputs["input_ids"].shape[1]

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            pad_token_id=tokenizer.eos_token_id,
        )

    response = tokenizer.decode(outputs[0, input_len:], skip_special_tokens=True)

    # Strip Qwen3 chain-of-thought tokens if present.
    if "</think>" in response:
        response = response.split("</think>")[-1].strip()

    return response.strip()


# ---------------------------------------------------------------------------
# 3. Answer extraction (identical to Exp 181).
# ---------------------------------------------------------------------------


def extract_final_number(text: str) -> int | None:
    """Extract the final numeric answer from an LLM response.

    **Detailed explanation for engineers:**
        Priority order: GSM8K #### format → "Answer: N" pattern → last number.
        Handles comma-separated numbers (e.g., "1,234" → 1234) and negatives.
    """
    m = re.search(r"####\s*(-?[\d,]+)", text)
    if m:
        try:
            return int(m.group(1).replace(",", ""))
        except ValueError:
            pass

    m = re.search(r"[Aa]nswer[:\s]+(-?[\d,]+)", text)
    if m:
        try:
            return int(m.group(1).replace(",", ""))
        except ValueError:
            pass

    numbers = re.findall(r"-?[\d,]+", text)
    if numbers:
        try:
            return int(numbers[-1].replace(",", ""))
        except ValueError:
            pass

    return None


# ---------------------------------------------------------------------------
# 4. Arithmetic constraint extraction and violation formatting (from Exp 181).
# ---------------------------------------------------------------------------


def extract_arithmetic_steps(response: str) -> list[dict[str, Any]]:
    """Find all 'a OP b = c' expressions in a response and verify each.

    **Detailed explanation for engineers:**
        Regex matches simple binary arithmetic expressions with a claimed result.
        Each is evaluated to check if the claim is correct (within 0.01 tolerance).
        Returns a list of step dicts with 'satisfied' flags for the repair loop.
    """
    steps: list[dict[str, Any]] = []
    pattern = re.compile(
        r"(-?[\d,]+(?:\.\d+)?)\s*"
        r"([+\-*/×x÷])\s*"
        r"(-?[\d,]+(?:\.\d+)?)\s*"
        r"=\s*(-?[\d,]+(?:\.\d+)?)"
    )
    for m in pattern.finditer(response):
        try:
            a = float(m.group(1).replace(",", ""))
            op = m.group(2)
            b = float(m.group(3).replace(",", ""))
            claimed = float(m.group(4).replace(",", ""))
        except ValueError:
            continue

        if op in ("×", "x"):
            op = "*"
        if op == "÷":
            op = "/"

        if op == "+":
            correct = a + b
        elif op == "-":
            correct = a - b
        elif op == "*":
            correct = a * b
        elif op == "/" and b != 0:
            correct = a / b
        else:
            continue

        # Coerce to int when all values are whole numbers for cleaner output.
        if a == int(a) and b == int(b) and claimed == int(claimed):
            a, b, claimed = int(a), int(b), int(claimed)
            if correct == int(correct):
                correct = int(correct)

        satisfied = abs(float(claimed) - float(correct)) < 0.01
        steps.append({
            "expression": f"{a} {op} {b}",
            "claimed": claimed,
            "correct": correct,
            "satisfied": satisfied,
        })
    return steps


def format_violations(arith_steps: list[dict[str, Any]]) -> str:
    """Convert arithmetic violations into natural-language repair feedback.

    **Detailed explanation for engineers:**
        Only includes violated steps. Does NOT reveal the ground-truth final
        answer — only corrects intermediate arithmetic. This is the Carnot
        value proposition: precise, constraint-guided feedback that guides the
        model toward a correct answer without simply giving it away.
    """
    violated = [s for s in arith_steps if not s.get("satisfied", True)]
    if not violated:
        return ""
    lines = ["Your answer contains arithmetic errors:"]
    for i, v in enumerate(violated, 1):
        lines.append(
            f"  {i}. You wrote {v['expression']} = {v['claimed']}, "
            f"but the correct result is {v['correct']}."
        )
    lines += [
        "",
        "Please recalculate step by step, fixing these errors. "
        "Give the final answer as a number.",
    ]
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# 5. Carnot VerifyRepairPipeline (verify-only mode check).
# ---------------------------------------------------------------------------


def verify_with_pipeline(question: str, response: str) -> dict[str, Any]:
    """Run VerifyRepairPipeline.verify() on a response (verify-only mode).

    **Detailed explanation for engineers:**
        Constructs a pipeline with no model (verify-only) and calls verify()
        with domain="arithmetic". Returns a compact dict with verified flag,
        constraint counts, and energy. Wraps all exceptions to avoid crashing
        the benchmark loop on unexpected pipeline errors.
    """
    try:
        from carnot.pipeline.verify_repair import VerifyRepairPipeline

        pipeline = VerifyRepairPipeline(
            model=None,
            domains=["arithmetic"],
            timeout_seconds=30.0,
        )
        vr = pipeline.verify(question, response, domain="arithmetic")
        return {
            "verified": vr.verified,
            "n_constraints": len(vr.constraints),
            "n_violations": len(vr.violations),
            "energy": vr.energy,
            "error": None,
        }
    except Exception as e:
        return {
            "verified": False,
            "n_constraints": 0,
            "n_violations": 0,
            "energy": 0.0,
            "error": str(e),
        }


