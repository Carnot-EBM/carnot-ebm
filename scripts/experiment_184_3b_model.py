# ---------------------------------------------------------------------------
# 10. Bootstrap CI utilities (from Exp 181).
# ---------------------------------------------------------------------------


def bootstrap_ci(
    correct_flags: list[bool],
    n_bootstrap: int = N_BOOTSTRAP,
    confidence: float = 0.95,
    seed: int = BOOTSTRAP_SEED_BASE,
) -> tuple[float, float, float]:
    """Compute accuracy and 95% bootstrap CI from correct/wrong flags.

    **Detailed explanation for engineers:**
        Non-parametric bootstrap: draws n_bootstrap samples of size N with
        replacement, computes accuracy for each, returns percentile CIs.
        Vectorised numpy for speed (all samples drawn as a 2D index matrix).

    Returns:
        (accuracy, ci_lower, ci_upper) as floats.
    """
    rng = np.random.default_rng(seed)
    arr = np.array(correct_flags, dtype=float)
    n = len(arr)
    point = float(np.mean(arr))
    if n == 0:
        return 0.0, 0.0, 0.0
    indices = rng.integers(0, n, size=(n_bootstrap, n))
    boot_means = arr[indices].mean(axis=1)
    alpha = 1.0 - confidence
    ci_lo = float(np.percentile(boot_means, 100 * alpha / 2))
    ci_hi = float(np.percentile(boot_means, 100 * (1 - alpha / 2)))
    return point, ci_lo, ci_hi


def bootstrap_delta_ci(
    base_flags: list[bool],
    repair_flags: list[bool],
    n_bootstrap: int = N_BOOTSTRAP,
    confidence: float = 0.95,
    seed: int = BOOTSTRAP_SEED_BASE + 1,
) -> tuple[float, float, float]:
    """Compute paired delta (repair - baseline) and 95% CI.

    **Detailed explanation for engineers:**
        Paired bootstrap: delta is computed per question before bootstrapping.
        This accounts for within-question correlation (the same question is
        harder for both modes), giving tighter CIs than unpaired bootstrap.

    Returns:
        (delta, ci_lower, ci_upper) as floats.
    """
    rng = np.random.default_rng(seed)
    base = np.array(base_flags, dtype=float)
    rep = np.array(repair_flags, dtype=float)
    delta_per_q = rep - base
    n = len(delta_per_q)
    if n == 0:
        return 0.0, 0.0, 0.0
    point = float(np.mean(delta_per_q))
    indices = rng.integers(0, n, size=(n_bootstrap, n))
    boot_deltas = delta_per_q[indices].mean(axis=1)
    alpha = 1.0 - confidence
    ci_lo = float(np.percentile(boot_deltas, 100 * alpha / 2))
    ci_hi = float(np.percentile(boot_deltas, 100 * (1 - alpha / 2)))
    return point, ci_lo, ci_hi


def two_proportion_z_test(
    n1_success: int, n1_total: int,
    n2_success: int, n2_total: int,
) -> tuple[float, float]:
    """One-sided two-proportion z-test: p1 > p2 (H1: adversarial delta > standard delta).

    **Detailed explanation for engineers:**
        Computes the z-statistic for the difference in two proportions and
        returns (z_stat, p_value_one_sided). Small p → reject H0 that the
        two proportions are equal in favour of H1 that p1 > p2.
        Uses pooled standard error under H0.

    Returns:
        (z_stat, p_value_one_sided) — p_value is one-sided (upper tail).
    """
    if n1_total == 0 or n2_total == 0:
        return 0.0, 1.0
    p1 = n1_success / n1_total
    p2 = n2_success / n2_total
    p_pool = (n1_success + n2_success) / (n1_total + n2_total)
    se = math.sqrt(p_pool * (1 - p_pool) * (1 / n1_total + 1 / n2_total))
    if se == 0:
        return 0.0, 1.0
    z = (p1 - p2) / se
    # One-sided p-value: P(Z > z) under H0.
    p_val = 1.0 - _normal_cdf(z)
    return z, p_val


def _normal_cdf(z: float) -> float:
    """Approximate normal CDF using math.erfc (no scipy needed)."""
    return 0.5 * math.erfc(-z / math.sqrt(2))


