def load_08b_comparison() -> dict[str, Any] | None:
    """Load the partial Exp 181 Qwen3.5-0.8B checkpoint for comparison.

    **Detailed explanation for engineers:**
        Exp 181 produced a checkpoint with the first 500 questions completed
        (results/exp181_ckpt_Qwen3.5-0.8B.json). We use this to compute the
        0.8B improvement delta (repair - baseline) for direct comparison with
        the 3B delta on the same N=200 standard split.

        To ensure a fair comparison, we use only the first 200 entries from
        the checkpoint (same N as this experiment). If the checkpoint is not
        found or contains fewer than 200 entries, we return None and note N/A.

    Returns:
        Dict with 'model', 'n', 'baseline_acc', 'repair_acc', 'delta',
        'ci_lower', 'ci_upper'; or None if checkpoint unavailable.
    """
    ckpt_path = RESULTS_DIR / "exp181_ckpt_Qwen3.5-0.8B.json"
    if not ckpt_path.exists():
        print("  0.8B comparison: checkpoint not found — will report N/A.")
        return None

    try:
        with open(ckpt_path) as f:
            ckpt = json.load(f)
    except Exception as e:
        print(f"  0.8B comparison: checkpoint load failed ({e}) — N/A.")
        return None

    base_entries = ckpt.get("baseline", [])
    repair_entries = ckpt.get("verify_repair", [])
    n = min(len(base_entries), len(repair_entries), N_STANDARD)

    if n < N_STANDARD:
        print(f"  0.8B comparison: only {n} entries in checkpoint (need {N_STANDARD}) — "
              f"using {n} entries.")

    if n == 0:
        return None

    base_flags = [r["correct"] for r in base_entries[:n]]
    repair_flags = [r["correct"] for r in repair_entries[:n]]

    base_acc = sum(base_flags) / n
    repair_acc = sum(repair_flags) / n
    delta, delta_lo, delta_hi = bootstrap_delta_ci(
        base_flags, repair_flags, seed=BOOTSTRAP_SEED_BASE + 10,
    )

    print(f"  0.8B comparison (N={n}): baseline={base_acc:.1%}, "
          f"repair={repair_acc:.1%}, Δ={delta:+.1%} [{delta_lo:+.1%}, {delta_hi:+.1%}]")

    return {
        "model": "Qwen3.5-0.8B",
        "source": "exp181_ckpt",
        "n": n,
        "baseline_accuracy": round(base_acc, 6),
        "repair_accuracy": round(repair_acc, 6),
        "improvement_delta": round(delta, 6),
        "ci_delta_lower": round(delta_lo, 6),
        "ci_delta_upper": round(delta_hi, 6),
    }


