# 8. Adversarial dataset loading (number_swapped, first N_ADVERSARIAL items).
# ---------------------------------------------------------------------------


def load_adversarial_questions(n: int = N_ADVERSARIAL) -> list[dict[str, Any]]:
    """Load the first `n` number-swapped adversarial questions from Exp 182 data.

    **Detailed explanation for engineers:**
        The Exp 182 dataset (adversarial_gsm8k_data_400.json) was generated by
        applying four perturbation variants to 400 synthetic GSM8K-style problems.
        We use the "number_swapped" variant which changes all numeric values while
        preserving logical structure — forcing the model to reason from scratch
        rather than pattern-matching memorised numeric patterns.

        Format: {perturbed_problem, correct_answer, template, ...}
        We rename keys to match the standard question format used by run_baseline()
        and run_verify_repair() via q_text_key="perturbed_problem".

    Returns:
        List of dicts with perturbed_problem, ground_truth, and metadata.
    """
    adv_path = RESULTS_DIR / "adversarial_gsm8k_data_400.json"
    if not adv_path.exists():
        print(f"FATAL: Adversarial dataset not found at {adv_path}")
        print("  Run Exp 182 first to generate: scripts/experiment_182_adversarial_live_gpu.py")
        sys.exit(1)

    with open(adv_path) as f:
        data = json.load(f)

    items = data["datasets"]["number_swapped"]
    selected = items[:n]

    questions: list[dict[str, Any]] = []
    for item in selected:
        questions.append({
            "perturbed_problem": item["perturbed_problem"],
            "ground_truth": item["correct_answer"],
            "original_problem": item.get("original_problem", ""),
            "template": item.get("template", ""),
            "source": "adversarial_number_swapped",
            "id": item.get("id", ""),
        })

    print(f"  Loaded {len(questions)} number-swapped adversarial questions from {adv_path.name}")
    return questions


