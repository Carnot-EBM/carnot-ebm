def run_baseline(
    question: dict[str, Any],
    model: Any,
    tokenizer: Any,
    q_text_key: str = "question",
) -> dict[str, Any]:
    """Mode 1 — Baseline: single forward pass, no verification.

    **Detailed explanation for engineers:**
        Single greedy generation per question. Extracts the final numeric
        answer and compares to ground truth. This is the control condition
        against which verify-repair improvement is measured.

    Args:
        question: Dict with q_text_key (question text) and 'ground_truth'.
        model: Loaded model on DEVICE_STR.
        tokenizer: Matching tokenizer.
        q_text_key: Key for the question text (differs for adversarial inputs).
    """
    t0 = time.time()
    q_text = question[q_text_key]
    prompt = (
        f"Question: {q_text}\n"
        f"Solve step by step. Give the final answer as a number.\n"
        f"Format:\nAnswer: <number>"
    )
    response = _generate(model, tokenizer, prompt, max_new_tokens=256)
    elapsed = time.time() - t0
    extracted = extract_final_number(response)
    correct = extracted is not None and extracted == question["ground_truth"]
    return {
        "mode": "baseline",
        "extracted_answer": extracted,
        "correct": correct,
        "time_s": round(elapsed, 3),
    }


def run_verify_repair(
    question: dict[str, Any],
    model: Any,
    tokenizer: Any,
    q_text_key: str = "question",
    max_repairs: int = MAX_REPAIR_ITERS,
) -> dict[str, Any]:
    """Mode 2 — Verify+Repair: iterative EBM-guided correction.

    **Detailed explanation for engineers:**
        Full pipeline:
          1. Generate initial response.
          2. Extract arithmetic step constraints and verify each one.
          3. If violations found: format natural-language feedback and
             re-prompt the model with the original question plus the feedback.
          4. Repeat up to max_repairs times or until no violations remain.

        "repaired" = initially wrong but finally correct (the key delta metric).
        This is what we compare across model sizes to test the hypothesis.

        At 3B scale (~65 tok/s on RTX 3090), each generation ≈ 4s at 256 tokens.
        Per-question worst case: 4 × 4s = 16s → 200 questions ≈ 53 min per split.
    """
    t0 = time.time()
    q_text = question[q_text_key]
    gt = question["ground_truth"]

    response = ""
    arith_steps: list[dict[str, Any]] = []
    extracted: int | None = None
    initial_correct = False
    initial_extracted = None
    total_constraints = 0
    total_violated = 0
    n_repairs = 0

    for iteration in range(max_repairs + 1):
        if iteration == 0:
            prompt = (
                f"Question: {q_text}\n"
                f"Solve step by step, showing all arithmetic. "
                f"Give the final answer as a number.\n"
                f"Format:\nAnswer: <number>"
            )
        else:
            feedback = format_violations(arith_steps)
            if not feedback:
                break  # All steps verified — no more repairs needed.
            prompt = (
                f"Question: {q_text}\n\n"
                f"Your previous answer was:\n{response}\n\n"
                f"However, verification found problems:\n{feedback}\n\n"
                f"Please recalculate step by step and give a corrected answer.\n"
                f"Format:\nAnswer: <number>"
            )

        response = _generate(model, tokenizer, prompt, max_new_tokens=256)
        extracted = extract_final_number(response)
        arith_steps = extract_arithmetic_steps(response)
        n_step_violated = sum(1 for s in arith_steps if not s["satisfied"])

        total_constraints += len(arith_steps)
        total_violated += n_step_violated

        if iteration == 0:
            initial_correct = extracted is not None and extracted == gt
            initial_extracted = extracted

        if n_step_violated == 0:
            break  # No violations in this response — converged.
        if iteration < max_repairs:
            n_repairs += 1

    elapsed = time.time() - t0
    final_correct = extracted is not None and extracted == gt

    return {
        "mode": "verify_repair",
        "extracted_answer": extracted,
        "correct": final_correct,
        "initial_correct": initial_correct,
        "initial_extracted": initial_extracted,
        "n_constraints": total_constraints,
        "n_violated": total_violated,
        "n_repairs": n_repairs,
        "repaired": not initial_correct and final_correct,
        "time_s": round(elapsed, 3),
    }


