# 4. Error categorization (Apple taxonomy + irrelevant-number detection)
# ---------------------------------------------------------------------------


def categorize_error(
    item: dict[str, Any],
    response: str,
    extracted: int | None,
    variant_key: str,
) -> str:
    """Classify the error type when a model gets a question wrong.

    **Detailed explanation for engineers:**
        Four categories following Apple's GSM-Symbolic taxonomy:

        1. **irrelevant_number_error**: Specific to injected-distractor variants.
           We extract any numbers embedded in the irrelevant sentence and check
           if the model's answer is close to one of them. If yes, the model was
           misled by the NoOp distractor. Threshold: within ±5 of a distractor
           number counts as a hit.

        2. **arithmetic_error**: The response contains at least one detectable
           arithmetic step violation (e.g., "3 * 15 = 46" when 3*15=45).
           Detected by re-parsing chain-of-thought.

        3. **reading_comprehension_error**: The answer is wildly off (more than
           3× the correct answer or less than 30% of it), or we can't extract
           any number at all — suggesting the model misread the problem.

        4. **logic_error**: Arithmetic is internally consistent and the answer
           is in a reasonable range, but it's still wrong — the model used the
           right arithmetic but assembled the wrong equation (missing a step,
           wrong operation, etc.).

    Args:
        item: Dataset item with fields perturbed_problem, correct_answer.
        response: The model's text response.
        extracted: The number we extracted from the response (None if unparseable).
        variant_key: One of "control", "number_swapped", "irrelevant_injected",
            "combined". Used to check for irrelevant-number errors.

    Returns:
        One of: "arithmetic_error", "irrelevant_number_error",
        "logic_error", "reading_comprehension_error".
    """
    gt = item["correct_answer"]

    # --- Irrelevant-number error detection ---
    if variant_key in ("irrelevant_injected", "combined") and extracted is not None:
        # Extract numbers from the perturbed problem that are NOT in the
        # original problem — these are the NoOp distractors.
        original_nums = set(re.findall(r"\d+", item.get("original_problem", "")))
        perturbed_nums = set(re.findall(r"\d+", item["perturbed_problem"]))
        injected_nums = perturbed_nums - original_nums
        for n_str in injected_nums:
            try:
                n = int(n_str)
                if n > 0 and abs(extracted - n) <= 5:
                    return "irrelevant_number_error"
            except ValueError:
                pass

    # --- Reading comprehension error: no number or wildly off ---
    if extracted is None:
        return "reading_comprehension_error"
    if gt != 0:
        ratio = abs(extracted) / abs(gt)
        if ratio > 3.0 or ratio < 0.3:
            return "reading_comprehension_error"

    # --- Arithmetic step error: re-parse chain-of-thought ---
    pattern = re.compile(
        r"(-?[\d,]+(?:\.\d+)?)\s*([+\-*/×x÷])\s*(-?[\d,]+(?:\.\d+)?)\s*=\s*(-?[\d,]+(?:\.\d+)?)"
    )
    for m in pattern.finditer(response):
        try:
            a = float(m.group(1).replace(",", ""))
            op = m.group(2)
            b = float(m.group(3).replace(",", ""))
            claimed = float(m.group(4).replace(",", ""))
        except ValueError:
            continue
        if op in ("×", "x"):
            op = "*"
        if op == "÷":
            op = "/"
        try:
            if op == "+":
                correct = a + b
            elif op == "-":
                correct = a - b
            elif op == "*":
                correct = a * b
            elif op == "/" and b != 0:
                correct = a / b
            else:
                continue
        except ZeroDivisionError:
            continue
        if abs(claimed - correct) > 0.01:
            return "arithmetic_error"

    # --- Logic error: internally consistent but wrong answer ---
    return "logic_error"


