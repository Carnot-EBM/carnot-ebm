"""Experiment 120: Adversarial GSM8K Baseline — LLM accuracy on adversarial variants.

**Researcher summary:**
    Measures baseline LLM accuracy on the four adversarial GSM8K variants
    generated by Experiment 119 (Apple GSM-Symbolic methodology). We test two
    target models — Qwen3.5-0.8B and Gemma4-E4B-it — on control, number-swapped,
    irrelevant-injected, and combined variants to quantify how much accuracy
    degrades under each adversarial perturbation.

    Apple found drops of up to 65% from adversarial perturbations. This
    experiment establishes the Carnot baseline: how badly do these models
    fail BEFORE any EBM-guided repair? Experiment 121 will apply the Carnot
    verify+repair pipeline to the same questions to show recovery.

**Detailed explanation for engineers:**
    Adversarial perturbation types (from Apple arxiv 2410.05229):

    1. **Control (GSM8K standard)**: Unmodified questions. Our accuracy
       baseline — models should do well here.

    2. **Number-swapped (GSM-Symbolic)**: Same problem structure, different
       numeric values. If the model truly *reasons*, it adapts and gets the
       new correct answer. If it *pattern-matches*, it hallucinates an answer
       near the original problem's numeric range — measuring memorization
       vs. reasoning.

    3. **Irrelevant-injected (GSM-NoOp)**: A sentence with an irrelevant
       number is inserted. Reasoning models ignore it; pattern-matching models
       incorporate it as a signal. This tests attentional focus.

    4. **Combined**: Both perturbations together — number swap + irrelevant
       injection. The harshest condition.

    Error categorization mirrors Apple's taxonomy:
    - **arithmetic_error**: The model made an incorrect calculation in its
      chain-of-thought (detected by verifying intermediate arithmetic steps).
    - **irrelevant_number_error**: The model's answer is close to a number
      that appeared as the irrelevant distractor — clear sign of NoOp failure.
    - **logic_error**: Arithmetic is correct but the model applied the wrong
      operations or missed/added steps (structural reasoning failure).
    - **reading_comprehension_error**: The model extracted/used the wrong
      numbers from the problem statement altogether.

    Bootstrap 95% confidence intervals (n=1000 resamples) quantify uncertainty
    in each accuracy estimate.

    Data flow:
    1. Load adversarial_gsm8k_data.json (or regenerate via Exp 119 logic).
    2. For each model × each variant (800 total inference calls per model):
       - Run inference (live LLM or simulation fallback).
       - Extract final numeric answer from response.
       - Compare to ground truth → correct/incorrect.
       - Categorize error type if incorrect.
    3. Compute accuracy + accuracy drop vs. control.
    4. Bootstrap CI on accuracy.
    5. Save to results/experiment_120_results.json.
    6. Print formatted comparison table.

Usage:
    JAX_PLATFORMS=cpu .venv/bin/python scripts/experiment_120_adversarial_baseline.py

Spec: REQ-VERIFY-001, REQ-VERIFY-002, SCENARIO-VERIFY-006
"""

