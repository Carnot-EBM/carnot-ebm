# 8. Per-variant inference loop
# ---------------------------------------------------------------------------


def run_variant(
    variant_key: str,
    items: list[dict[str, Any]],
    model_name: str,
    tokenizer: Any,
    model_obj: Any,
    device: str,
    use_live: bool,
    sim_rng: random.Random,
) -> list[dict[str, Any]]:
    """Run inference on all items in a single variant dataset.

    **Detailed explanation for engineers:**
        Iterates over all 200 items for this variant. For each item:
        1. Build the inference prompt using the perturbed problem text.
        2. Generate a response (live model or simulation).
        3. Extract the final number from the response.
        4. Check correctness against item["correct_answer"].
        5. If incorrect, categorize the error type.

        Returns a list of per-item result dicts for downstream aggregation.

    Args:
        variant_key: One of the four variant keys ("control", etc.).
        items: List of 200 dataset items for this variant.
        model_name: Used for simulation error rate selection.
        tokenizer, model_obj, device: Live inference components (or None).
        use_live: Whether to call the real model (vs. simulation).
        sim_rng: Random generator for simulation reproducibility.

    Returns:
        List of result dicts: {id, correct, extracted, error_type, time_s}.
    """
    results: list[dict[str, Any]] = []
    n = len(items)

    for i, item in enumerate(items):
        if (i + 1) % 50 == 0:
            print(f"      [{variant_key}] {i + 1}/{n}...")

        t0 = time.time()
        prompt = (
            f"Question: {item['perturbed_problem']}\n"
            f"Solve step by step. Give the final answer as a number.\n"
            f"Format:\nAnswer: <number>"
        )

        if use_live:
            response = generate_response(prompt, tokenizer, model_obj, device)
        else:
            response = simulate_response_adversarial(
                item, model_name, variant_key, sim_rng
            )

        elapsed = time.time() - t0
        extracted = extract_final_number(response)
        gt = item["correct_answer"]
        correct = extracted is not None and extracted == gt

        error_type: str | None = None
        if not correct:
            error_type = categorize_error(item, response, extracted, variant_key)

        results.append({
            "id": item["id"],
            "correct": correct,
            "extracted_answer": extracted,
            "ground_truth": gt,
            "error_type": error_type,
            "time_s": elapsed,
        })

    return results


# ---------------------------------------------------------------------------
# 9. Aggregate metrics for a single variant
# ---------------------------------------------------------------------------


def compute_variant_metrics(
    results: list[dict[str, Any]],
    control_accuracy: float | None = None,
) -> dict[str, Any]:
    """Aggregate per-item results into variant-level metrics.

    **Detailed explanation for engineers:**
        Computes:
        - accuracy: fraction correct (0.0–1.0)
        - accuracy_pct: as a percentage (0–100)
        - accuracy_drop_pp: accuracy drop versus control in percentage points
          (positive = worse than control). None if no control accuracy given.
        - error_counts: breakdown by error type
        - error_pcts: same as percentages of total items
        - ci_95_lo, ci_95_hi: 95% bootstrap CI bounds on accuracy

    Args:
        results: List of per-item result dicts from run_variant().
        control_accuracy: Accuracy on the control variant (fraction), or
            None if this IS the control.

    Returns:
        Dict with all metric fields.
    """
    n = len(results)
    correct_flags = [r["correct"] for r in results]
    accuracy = sum(correct_flags) / n if n > 0 else 0.0

    error_types = ["arithmetic_error", "irrelevant_number_error",
                   "logic_error", "reading_comprehension_error"]
    error_counts: dict[str, int] = {et: 0 for et in error_types}
    for r in results:
        if not r["correct"] and r["error_type"]:
            error_counts[r["error_type"]] = error_counts.get(r["error_type"], 0) + 1

    ci_lo, ci_hi = bootstrap_ci(correct_flags)

    accuracy_drop: float | None = None
    if control_accuracy is not None:
        accuracy_drop = (control_accuracy - accuracy) * 100.0

    return {
        "n_questions": n,
        "n_correct": sum(correct_flags),
        "accuracy": round(accuracy, 4),
        "accuracy_pct": round(accuracy * 100, 2),
        "accuracy_drop_pp": round(accuracy_drop, 2) if accuracy_drop is not None else None,
        "ci_95_lo": round(ci_lo, 4),
        "ci_95_hi": round(ci_hi, 4),
        "error_counts": error_counts,
        "error_pcts": {et: round(c / n * 100, 1) for et, c in error_counts.items()},
    }


# ---------------------------------------------------------------------------
# 10. Main experiment loop
# ---------------------------------------------------------------------------


def run_experiment() -> dict[str, Any]:
    """Run the full Experiment 120 adversarial baseline measurement.

    **Detailed explanation for engineers:**
        Outer loop: models. Inner loop: variants.
        For each model, we:
        1. Attempt to load the real HuggingFace model.
        2. Run all 4 variants (control first to compute accuracy drop).
        3. Aggregate metrics.
        4. Unload the model to free memory for the next model.

        If the model cannot be loaded, we use simulation with adversarial-
        accurate error patterns (see simulate_response_adversarial docstring).

        Progress is printed every 50 questions and at variant boundaries.

    Returns:
        Full results dict ready for JSON serialization.
    """
    print("=" * 70)
    print("Experiment 120: Adversarial GSM8K Baseline")
    print("=" * 70)

    # Load adversarial data.
    print("\n[1] Loading adversarial datasets...")
    datasets = load_adversarial_data()

    all_model_results: dict[str, Any] = {}

    for model_cfg in MODEL_CONFIGS:
        model_name = model_cfg["name"]
        print(f"\n[Model: {model_name}]")
        print(f"  Attempting to load model...")

        tokenizer, model_obj, device, use_live = load_model(model_cfg)
        if use_live:
            print(f"  Model loaded on {device}. Running live inference.")
        else:
            print(f"  Model not loaded. Using simulation with adversarial error patterns.")

        # Seeded RNG for reproducible simulation.
        sim_seed = sum(ord(c) for c in model_name) + 120
        sim_rng = random.Random(sim_seed)

        model_variant_results: dict[str, Any] = {}
        control_accuracy: float | None = None

        for variant_key, variant_label in VARIANTS:
            print(f"\n  Variant: {variant_label}")
            items = datasets[variant_key]

            t_start = time.time()
            per_item = run_variant(
                variant_key, items, model_name,
                tokenizer, model_obj, device, use_live, sim_rng,
            )
            t_end = time.time()

            metrics = compute_variant_metrics(per_item, control_accuracy)

            if variant_key == "control":
                control_accuracy = metrics["accuracy"]

            print(
                f"    Accuracy: {metrics['accuracy_pct']:.1f}%"
                + (
                    f"  (drop: {metrics['accuracy_drop_pp']:+.1f} pp)"
                    if metrics["accuracy_drop_pp"] is not None else ""
                )
                + f"  [95% CI: {metrics['ci_95_lo']*100:.1f}–{metrics['ci_95_hi']*100:.1f}%]"
                + f"  ({t_end - t_start:.1f}s)"
            )

            model_variant_results[variant_key] = {
                "variant_label": variant_label,
                "metrics": metrics,
                "inference_mode": "live" if use_live else "simulated",
            }

        # Unload model to reclaim memory.
        if use_live:
            unload_model(model_obj, tokenizer, device)

        all_model_results[model_name] = {
            "model_config": {
                "name": model_name,
                "candidates": model_cfg["candidates"],
            },
            "inference_mode": "live" if use_live else "simulated",
            "variants": model_variant_results,
        }

    return {
        "experiment": 120,
        "description": "Adversarial GSM8K Baseline: LLM accuracy on adversarial variants",
        "models": all_model_results,
        "variant_keys": [v[0] for v in VARIANTS],
    }


# ---------------------------------------------------------------------------
# 11. Comparison table printer
# ---------------------------------------------------------------------------


def print_comparison_table(results: dict[str, Any]) -> None:
    """Print a formatted model × variant × accuracy comparison table.

    **Detailed explanation for engineers:**
        Renders a human-readable ASCII table showing accuracy (%) and
        accuracy drop (pp) for each model × variant combination. Also
        shows the 95% CI in parentheses. A trailing row shows the
        average across models for each variant.

        Column layout:
          Model | Control | Number-swapped | Irrelevant-injected | Combined
    """
    models = list(results["models"].keys())
    variant_keys = results["variant_keys"]
    variant_labels = {v[0]: v[1] for v in VARIANTS}

    # Header.
    col_w = 26
    hdr = f"{'Model':<22}" + "".join(f"{variant_labels[k][:col_w]:<{col_w}}" for k in variant_keys)
    print("\n" + "=" * (22 + col_w * len(variant_keys)))
    print("EXPERIMENT 120 — ADVERSARIAL BASELINE RESULTS")
    print("=" * (22 + col_w * len(variant_keys)))
    print(hdr)
    print("-" * (22 + col_w * len(variant_keys)))

    for model_name in models:
        mdata = results["models"][model_name]
        row = f"{model_name:<22}"
        for vk in variant_keys:
            vdata = mdata["variants"][vk]
            m = vdata["metrics"]
            acc = m["accuracy_pct"]
            drop = m["accuracy_drop_pp"]
            ci_lo = m["ci_95_lo"] * 100
            ci_hi = m["ci_95_hi"] * 100
            drop_str = f" ({drop:+.1f}pp)" if drop is not None else ""
            cell = f"{acc:.1f}%{drop_str}"
            row += f"{cell:<{col_w}}"
        print(row)
        # Error breakdown sub-row.
        row2 = f"  {'(errors)':<20}"
        for vk in variant_keys:
            vdata = mdata["variants"][vk]
            m = vdata["metrics"]
            ec = m["error_counts"]
            arith = ec.get("arithmetic_error", 0)
            irrel = ec.get("irrelevant_number_error", 0)
            logic = ec.get("logic_error", 0)
            read_ = ec.get("reading_comprehension_error", 0)
            cell = f"A:{arith} I:{irrel} L:{logic} R:{read_}"
            row2 += f"{cell:<{col_w}}"
        print(row2)
        print()

    print("-" * (22 + col_w * len(variant_keys)))
    print("Error key: A=arithmetic  I=irrelevant_number  L=logic  R=reading_comprehension")
    print("Drop = percentage-point drop vs. control (positive = worse)")
    print()


# ---------------------------------------------------------------------------
# 12. Entry point
# ---------------------------------------------------------------------------


def main() -> None:
    """Main entry point for Experiment 120."""
    t_start = time.time()

    results = run_experiment()

    # Save results.
    print(f"\n[Saving results to {OUTPUT_PATH}]")
    with open(OUTPUT_PATH, "w") as f:
        json.dump(results, f, indent=2)
    print(f"  Saved {OUTPUT_PATH.stat().st_size // 1024} KB.")

    # Print comparison table.
    print_comparison_table(results)

    elapsed = time.time() - t_start
    print(f"Experiment 120 complete in {elapsed:.1f}s.")
    print(f"Results: {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
