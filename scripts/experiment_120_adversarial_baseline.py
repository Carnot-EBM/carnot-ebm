def generate_response(
    prompt: str,
    tokenizer: Any,
    model: Any,
    device: str,
    max_new_tokens: int = 256,
) -> str:
    """Generate a response from a loaded HuggingFace causal LM.

    **Detailed explanation for engineers:**
        Uses greedy decoding (do_sample=False) for reproducibility. Applies
        the model's chat template if available. Strips <think>...</think>
        reasoning tokens from Qwen models before returning.
    """
    import torch

    messages = [{"role": "user", "content": prompt}]
    try:
        text = tokenizer.apply_chat_template(
            messages, tokenize=False, add_generation_prompt=True,
            enable_thinking=False,
        )
    except TypeError:
        try:
            text = tokenizer.apply_chat_template(
                messages, tokenize=False, add_generation_prompt=True,
            )
        except Exception:
            text = prompt

    inputs = tokenizer(text, return_tensors="pt")
    if device == "cuda":
        inputs = {k: v.cuda() for k, v in inputs.items()}

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            pad_token_id=tokenizer.eos_token_id,
        )

    response = tokenizer.decode(
        outputs[0, inputs["input_ids"].shape[1]:],
        skip_special_tokens=True,
    )

    # Strip Qwen thinking tokens.
    if "</think>" in response:
        response = response.split("</think>")[-1].strip()

    return response


def simulate_response_adversarial(
    item: dict[str, Any],
    model_name: str,
    variant_key: str,
    rng: random.Random,
) -> str:
    """Simulate an LLM response with adversarial-specific error patterns.

    **Detailed explanation for engineers:**
        When a real LLM cannot be loaded (no torch, no model files, ROCm hang),
        we simulate responses that exhibit the same degradation patterns Apple
        documented for small models.

        Base error rates (from Apple paper findings on similar-scale models):
        - Qwen3.5-0.8B: ~30% error on control, up to 65% on combined.
        - Gemma4-E4B-it: ~25% error on control, up to 55% on combined.

        Per-variant error rate multipliers (derived from Apple's reported
        degradation curves for 0.5B–1B scale models):
        - Control: 1.0× (baseline)
        - Number-swapped: 1.8× (significant degradation expected)
        - Irrelevant-injected: 1.5× (moderate degradation)
        - Combined: 2.2× (highest degradation)

        When the model errs on an irrelevant-injected or combined variant,
        50% of errors are "NoOp" errors — the model uses the injected number
        in its calculation. This matches Apple's finding that small models
        incorporate ~half of all irrelevant distractors.

    Args:
        item: Dataset item with perturbed_problem, correct_answer, etc.
        model_name: Used to select base error rate.
        variant_key: Determines error rate multiplier.
        rng: Seeded Random for reproducibility.

    Returns:
        Simulated chain-of-thought response string.
    """
    gt = item["correct_answer"]

    # Base error rates per model family.
    if "qwen" in model_name.lower():
        base_error = 0.30
    else:
        base_error = 0.25

    # Adversarial multipliers — small models degrade more on combined.
    multipliers = {
        "control": 1.0,
        "number_swapped": 1.8,
        "irrelevant_injected": 1.5,
        "combined": 2.2,
    }
    error_rate = min(0.90, base_error * multipliers.get(variant_key, 1.0))

    is_correct = rng.random() > error_rate

    if is_correct:
        step1 = rng.randint(1, max(1, abs(gt) // 2 + 1))
        step2 = gt - step1
        return (
            f"Let me solve step by step.\n"
            f"First: {step1}\n"
            f"Then: {step1} + {step2} = {gt}\n"
            f"Answer: {gt}"
        )

    # --- Determine error type ---
    # For injected variants, 50% of errors are NoOp (irrelevant-number) errors.
    if variant_key in ("irrelevant_injected", "combined"):
        if rng.random() < 0.50:
            # Extract injected numbers from the perturbed problem.
            original_nums = set(re.findall(r"\d+", item.get("original_problem", "")))
            perturbed_nums = list(set(re.findall(r"\d+", item["perturbed_problem"])) - original_nums)
            if perturbed_nums:
                distractor = int(rng.choice(perturbed_nums))
                # Model uses distractor in calculation, produces wrong answer.
                wrong = gt + distractor if rng.random() < 0.5 else abs(gt - distractor)
                return (
                    f"Looking at all the numbers in the problem.\n"
                    f"I need to account for {distractor} as well.\n"
                    f"My calculation gives: {wrong}\n"
                    f"Answer: {wrong}"
                )

    # Regular error types: arithmetic, logic, reading.
    error_type = rng.choices(
        ["arithmetic", "logic", "reading"], weights=[50, 35, 15], k=1
    )[0]

    if error_type == "arithmetic":
        step1 = rng.randint(1, max(1, abs(gt) // 2 + 1))
        step2 = gt - step1
        wrong = step1 + step2 + rng.choice([-3, -2, -1, 1, 2, 3])
        return (
            f"Let me solve step by step.\n"
            f"First: {step1}\n"
            f"Then: {step1} + {step2} = {wrong}\n"
            f"Answer: {wrong}"
        )
    elif error_type == "logic":
        offset = rng.choice([-20, -10, -5, 5, 10, 20])
        wrong = gt + offset
        return (
            f"Let me work through this.\n"
            f"The result is {wrong}.\n"
            f"Answer: {wrong}"
        )
    else:
        wrong = gt * rng.choice([2, 3]) + rng.randint(-50, 50)
        return (
            f"I think the answer is {wrong}.\n"
            f"Answer: {wrong}"
        )


# ---------------------------------------------------------------------------
# 7. Bootstrap confidence intervals
# ---------------------------------------------------------------------------


def bootstrap_ci(
    correct_flags: list[bool],
    n_resamples: int = 1000,
    confidence: float = 0.95,
    rng_seed: int = 120,
) -> tuple[float, float]:
    """Compute a bootstrap confidence interval for accuracy.

    **Detailed explanation for engineers:**
        Bootstrap resampling estimates the distribution of our accuracy
        estimate without assuming normality. We resample the boolean correct/
        incorrect array n_resamples times (with replacement), computing the
        mean each time. The confidence interval is the (alpha/2, 1-alpha/2)
        percentile range of that distribution.

        This is the standard non-parametric bootstrap — no assumptions about
        the underlying distribution, robust for binary outcomes.

    Args:
        correct_flags: List of bool (True = correct answer for that question).
        n_resamples: Number of bootstrap samples (default 1000).
        confidence: Confidence level (default 0.95 → 95% CI).
        rng_seed: Seed for reproducibility.

    Returns:
        Tuple (lower_bound, upper_bound) as fractions in [0, 1].
    """
    arr = np.array(correct_flags, dtype=float)
    n = len(arr)
    rng = np.random.default_rng(rng_seed)

    sample_means = np.empty(n_resamples)
    for i in range(n_resamples):
        idx = rng.integers(0, n, size=n)
        sample_means[i] = arr[idx].mean()

    alpha = 1.0 - confidence
    lo = float(np.percentile(sample_means, 100 * alpha / 2))
    hi = float(np.percentile(sample_means, 100 * (1 - alpha / 2)))
    return lo, hi


