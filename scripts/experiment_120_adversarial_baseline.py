def load_adversarial_data() -> dict[str, list[dict[str, Any]]]:
    """Load adversarial datasets from results/adversarial_gsm8k_data.json.

    **Detailed explanation for engineers:**
        Tries to load the pre-generated adversarial datasets from Experiment
        119. If the file is not found, regenerates it by importing and running
        the Experiment 119 generation logic inline. This ensures the experiment
        is self-contained: you can delete the intermediate JSON and re-run.

        Returns a dict with keys: "control", "number_swapped",
        "irrelevant_injected", "combined" — each a list of 200 item dicts
        with fields: id, perturbed_problem, correct_answer, perturbation.
    """
    if ADVERSARIAL_DATA_PATH.exists():
        print(f"  Loading adversarial data from {ADVERSARIAL_DATA_PATH}...")
        with open(ADVERSARIAL_DATA_PATH) as f:
            raw = json.load(f)
        datasets = raw["datasets"]
        print(f"  Loaded: {', '.join(f'{k}={len(v)}' for k, v in datasets.items())}")
        return datasets

    # Fallback: regenerate using Exp 119 generation code.
    print("  adversarial_gsm8k_data.json not found. Regenerating via Exp 119 logic...")
    try:
        import subprocess
        result = subprocess.run(
            [sys.executable, str(REPO_ROOT / "scripts/experiment_119_adversarial_gsm8k.py")],
            capture_output=True, text=True, timeout=120,
        )
        if result.returncode == 0 and ADVERSARIAL_DATA_PATH.exists():
            print("  Regenerated successfully.")
            return load_adversarial_data()
        else:
            print(f"  Regeneration failed (rc={result.returncode}): {result.stderr[:200]}")
    except Exception as e:
        print(f"  Regeneration error: {e}")

    raise FileNotFoundError(
        f"Could not load or regenerate {ADVERSARIAL_DATA_PATH}. "
        "Run scripts/experiment_119_adversarial_gsm8k.py first."
    )


# ---------------------------------------------------------------------------
# 3. Number extraction from LLM responses
# ---------------------------------------------------------------------------


def extract_final_number(text: str) -> int | None:
    """Extract the final numeric answer from an LLM response.

    **Detailed explanation for engineers:**
        Tries GSM8K "#### <number>" format first, then "Answer: <number>",
        then the last number in the text. Handles commas (e.g., "1,234" → 1234)
        and negative numbers. Returns None if no number is found.
    """
    # GSM8K "####" format.
    match = re.search(r"####\s*(-?[\d,]+)", text)
    if match:
        try:
            return int(match.group(1).replace(",", ""))
        except ValueError:
            pass

    # "Answer: <number>" or "answer is <number>".
    match = re.search(r"[Aa]nswer[:\s]+(-?[\d,]+)", text)
    if match:
        try:
            return int(match.group(1).replace(",", ""))
        except ValueError:
            pass

    # Last number in text (fallback).
    numbers = re.findall(r"-?[\d,]+", text)
    if numbers:
        try:
            return int(numbers[-1].replace(",", ""))
        except ValueError:
            pass

    return None


