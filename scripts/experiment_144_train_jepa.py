"""Experiment 144: Train JEPA Violation Predictor on Exp 143 pairs.

**Researcher summary:**
    Loads the (partial_response_embedding, final_violated) pairs collected by
    Exp 143, trains the Tier 3 JEPA MLP predictor, reports per-domain AUROC,
    and saves the trained model to results/jepa_predictor.safetensors.

**What this experiment tests:**
    - Can a small MLP (256→64→32→3) learn to predict constraint violations from
      partial-response embeddings generated by RandomProjection?
    - What is the macro AUROC on a held-out val set?
    - Target: macro AUROC > 0.65 (Tier 3 spec threshold).

**Results are written to:**
    - results/jepa_predictor.safetensors — trained model parameters
    - stdout — per-domain AUROC, precision, recall, confusion matrix analysis

Usage:
    JAX_PLATFORMS=cpu python scripts/experiment_144_train_jepa.py

Spec: REQ-JEPA-001, SCENARIO-JEPA-003
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

import numpy as np

# Ensure the package root is on the path when run from project root.
sys.path.insert(0, str(Path(__file__).parent.parent / "python"))

from carnot.pipeline.jepa_predictor import DOMAINS, JEPAViolationPredictor

TRAINING_DATA_PATH = Path("results/jepa_training_pairs.json")
MODEL_OUTPUT_PATH = Path("results/jepa_predictor.safetensors")

AUROC_TARGET = 0.65  # Tier 3 spec threshold (macro AUROC)


def load_pairs(path: Path) -> list[dict]:
    """Load training pairs from JSON file.

    Args:
        path: Path to jepa_training_pairs.json.

    Returns:
        List of pair dicts with embedding and per-domain violation labels.
    """
    with open(path) as f:
        data = json.load(f)
    pairs = data["pairs"]
    print(f"Loaded {len(pairs)} training pairs from {path}")
    return pairs


def print_data_stats(pairs: list[dict]) -> None:
    """Print dataset statistics: class balance per domain.

    Args:
        pairs: List of training pairs.
    """
    total = len(pairs)
    print("\n--- Dataset Statistics ---")
    for domain in DOMAINS:
        key = f"violated_{domain}"
        n_pos = sum(1 for p in pairs if p.get(key, False))
        n_neg = total - n_pos
        pct = 100.0 * n_pos / total if total > 0 else 0.0
        print(f"  {domain}: {n_pos} violated ({pct:.1f}%), {n_neg} clean")
    n_any = sum(1 for p in pairs if p.get("any_violated", False))
    print(f"  any_violated: {n_any}/{total} = {100.0 * n_any / total:.1f}%")


def print_training_log(log: dict) -> None:
    """Print training loss curve summary and validation metrics.

    Args:
        log: Training log returned by JEPAViolationPredictor.train().
    """
    train_losses = log["train_losses"]
    val_losses = log["val_losses"]
    n_epochs = len(train_losses)

    print("\n--- Training Summary ---")
    print(f"  Epochs: {n_epochs}")
    print(f"  n_train: {log['n_train']}, n_val: {log['n_val']}")
    print(f"  Train loss: {train_losses[0]:.4f} → {train_losses[-1]:.4f}")
    print(f"  Val   loss: {val_losses[0]:.4f} → {val_losses[-1]:.4f}")

    # Print loss every 10 epochs.
    print("\n  Epoch  Train    Val")
    for i in range(0, n_epochs, max(1, n_epochs // 10)):
        print(f"  {i+1:5d}  {train_losses[i]:.4f}  {val_losses[i]:.4f}")
    print(f"  {n_epochs:5d}  {train_losses[-1]:.4f}  {val_losses[-1]:.4f}")


def print_metrics(log: dict) -> None:
    """Print per-domain AUROC, precision, recall, and macro AUROC.

    Args:
        log: Training log returned by JEPAViolationPredictor.train().
    """
    auroc = log["val_auroc_per_domain"]
    prec = log["precision_at_05"]
    recall = log["recall_at_05"]
    macro = log["macro_auroc"]

    print("\n--- Validation Metrics (threshold=0.5) ---")
    print(f"  {'Domain':<12}  AUROC    Precision  Recall")
    print(f"  {'-'*12}  {'------':<8} {'--------':<10} {'------'}")
    for domain in DOMAINS:
        print(
            f"  {domain:<12}  {auroc[domain]:.4f}  {prec[domain]:.4f}     {recall[domain]:.4f}"
        )
    print(f"  {'Macro':<12}  {macro:.4f}")


def print_confusion_note(log: dict) -> None:
    """Flag false negative risk per domain (false negatives are costly).

    A false negative = predictor says 'safe' but response actually violates.
    Recall = TP / (TP + FN); low recall means many false negatives.

    Args:
        log: Training log returned by JEPAViolationPredictor.train().
    """
    recall = log["recall_at_05"]
    print("\n--- False Negative Analysis (lower recall = more missed violations) ---")
    for domain in DOMAINS:
        r = recall[domain]
        flag = " *** LOW RECALL - missed violations are high! ***" if r < 0.5 else ""
        print(f"  {domain}: recall={r:.4f}{flag}")


def main() -> None:
    """Run Experiment 144: train JEPA predictor and save model."""
    print("=" * 60)
    print("Experiment 144: Train JEPA Violation Predictor")
    print("=" * 60)

    if not TRAINING_DATA_PATH.exists():
        print(f"ERROR: Training data not found at {TRAINING_DATA_PATH}")
        print("Run Exp 143 first to generate training pairs.")
        sys.exit(1)

    # Load data.
    pairs = load_pairs(TRAINING_DATA_PATH)
    print_data_stats(pairs)

    # Train.
    print("\n--- Training ---")
    predictor = JEPAViolationPredictor(seed=42)
    log = predictor.train(
        pairs,
        n_epochs=50,
        lr=1e-3,
        batch_size=64,
        val_fraction=0.2,
        seed=42,
    )

    # Report results.
    print_training_log(log)
    print_metrics(log)
    print_confusion_note(log)

    # Check target.
    macro_auroc = log["macro_auroc"]
    print(f"\n--- Target Check ---")
    print(f"  Target: macro AUROC > {AUROC_TARGET}")
    print(f"  Actual: macro AUROC = {macro_auroc:.4f}")
    if macro_auroc > AUROC_TARGET:
        print("  RESULT: TARGET MET ✓")
    else:
        print(
            f"  RESULT: TARGET NOT MET — AUROC {macro_auroc:.4f} <= {AUROC_TARGET}."
        )
        print(
            "  NOTE: With only arithmetic violations in training data, code/logic AUROC = 0.5"
        )
        print(
            "  (undefined — no positives in those domains). This is expected for Exp 143 data."
        )

    # Save model.
    MODEL_OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    predictor.save(str(MODEL_OUTPUT_PATH))
    print(f"\nModel saved to: {MODEL_OUTPUT_PATH}")
    size_kb = MODEL_OUTPUT_PATH.stat().st_size / 1024
    print(f"File size: {size_kb:.1f} KB")

    # Verify round-trip load.
    loaded = JEPAViolationPredictor()
    loaded.load(str(MODEL_OUTPUT_PATH))
    print("\nRound-trip load verified ✓")

    print("\nExperiment 144 complete.")


if __name__ == "__main__":
    main()
