# ---------------------------------------------------------------------------
# 7. Main: run Ising-guided vs random fuzzing and print comparison
# ---------------------------------------------------------------------------

def main() -> int:
    """Run Ising-guided and random fuzzing on all 8 function pairs."""
    print("=" * 78)
    print("EXPERIMENT 54: Ising-Guided Fuzzing for Differential Testing")
    print("  Generate adversarial test inputs via Ising energy landscape")
    print("  Compare: Ising-guided sampling vs uniform random fuzzing")
    print("=" * 78)

    start = time.time()
    pairs = get_function_pairs()
    n_samples = 100
    rng = np.random.default_rng(54)

    # Column headers.
    print(f"\n{'#':<3} {'Bug Type':<28} {'Ising':>6} {'Rand':>6} "
          f"{'Ising Uniq':>10} {'Rand Uniq':>10} "
          f"{'Ising T1':>9} {'Rand T1':>9}")
    print("-" * 105)

    ising_total_bugs = 0
    rand_total_bugs = 0
    ising_total_uniq = 0
    rand_total_uniq = 0
    ising_wins = 0
    rand_wins = 0
    ties = 0

    results: list[dict[str, Any]] = []

    for i, pair in enumerate(pairs):
        name = pair["name"]
        buggy = pair["buggy"]
        reference = pair["reference"]
        params = pair["params"]

        # --- Ising-guided fuzzing ---
        ising_result = ising_fuzz(
            buggy, reference, params,
            n_samples=n_samples,
            edge_cases=pair.get("edge_cases"),
            seed=54 + i,
        )

        # --- Random fuzzing (same number of inputs) ---
        rand_inputs = random_inputs(params, n_samples, rng)
        rand_result = differential_fuzz(buggy, reference, rand_inputs)

        # Tally.
        ising_total_bugs += ising_result.bugs_found
        rand_total_bugs += rand_result.bugs_found
        ising_total_uniq += len(ising_result.unique_sigs)
        rand_total_uniq += len(rand_result.unique_sigs)

        if ising_result.bugs_found > rand_result.bugs_found:
            ising_wins += 1
        elif rand_result.bugs_found > ising_result.bugs_found:
            rand_wins += 1
        else:
            ties += 1

        # Format time-to-first-bug.
        it1 = f"{ising_result.time_to_first_bug:.4f}s" if ising_result.time_to_first_bug is not None else "N/A"
        rt1 = f"{rand_result.time_to_first_bug:.4f}s" if rand_result.time_to_first_bug is not None else "N/A"

        print(
            f"{i+1:<3} {pair['bug_type']:<28} "
            f"{ising_result.bugs_found:>6} {rand_result.bugs_found:>6} "
            f"{len(ising_result.unique_sigs):>10} {len(rand_result.unique_sigs):>10} "
            f"{it1:>9} {rt1:>9}"
        )

        # Show a sample disagreement for each pair.
        sample_errors = ising_result.errors[:1] or rand_result.errors[:1]
        for inp, buggy_out, ref_out in sample_errors:
            inp_short = str(inp)[:60]
            print(f"    example: {inp_short}")
            print(f"    buggy={_short_repr(buggy_out)}  ref={_short_repr(ref_out)}")

        results.append({
            "name": name,
            "bug_type": pair["bug_type"],
            "ising_bugs": ising_result.bugs_found,
            "rand_bugs": rand_result.bugs_found,
            "ising_uniq": len(ising_result.unique_sigs),
            "rand_uniq": len(rand_result.unique_sigs),
            "ising_t1": ising_result.time_to_first_bug,
            "rand_t1": rand_result.time_to_first_bug,
        })

    # --- Summary ---
    elapsed = time.time() - start
    sep = "=" * 78

    print(f"\n{sep}")
    print(f"EXPERIMENT 54 RESULTS ({elapsed:.1f}s)")
    print(sep)

    print(f"\n  Function pairs tested:      {len(pairs)}")
    print(f"  Inputs per method:          {n_samples}")
    print(f"\n  Total bugs found (Ising):   {ising_total_bugs}")
    print(f"  Total bugs found (Random):  {rand_total_bugs}")
    print(f"  Unique signatures (Ising):  {ising_total_uniq}")
    print(f"  Unique signatures (Random): {rand_total_uniq}")
    print(f"\n  Ising wins:                 {ising_wins}")
    print(f"  Random wins:                {rand_wins}")
    print(f"  Ties:                       {ties}")

    # Per-pair comparison.
    print(f"\n  Per-pair comparison:")
    for r in results:
        delta = r["ising_bugs"] - r["rand_bugs"]
        winner = "ISING" if delta > 0 else ("RANDOM" if delta < 0 else "TIE")
        print(f"    {r['bug_type']:<28} delta={delta:+4d}  [{winner}]")

    # Verdict.
    if ising_total_bugs > rand_total_bugs:
        improvement = (ising_total_bugs - rand_total_bugs) / max(rand_total_bugs, 1) * 100
        print(f"\n  VERDICT: Ising-guided fuzzing found {improvement:.0f}% more bugs")
        print(f"  than uniform random fuzzing over the same number of inputs.")
        print(f"  The energy landscape successfully biases sampling toward")
        print(f"  edge-case inputs that trigger differential behavior.")
    elif ising_total_bugs == rand_total_bugs:
        print(f"\n  VERDICT: Both methods found the same number of bugs.")
        if ising_total_uniq > rand_total_uniq:
            print(f"  However, Ising found more UNIQUE bug signatures")
            print(f"  ({ising_total_uniq} vs {rand_total_uniq}), suggesting")
            print(f"  better coverage of distinct failure modes.")
        else:
            print(f"  Ising-guided fuzzing did not outperform random on this")
            print(f"  test suite.")
    else:
        print(f"\n  VERDICT: Random fuzzing found more bugs ({rand_total_bugs})")
        print(f"  vs Ising ({ising_total_bugs}). The energy landscape encoding")
        print(f"  may need tuning for these specific bug classes.")

    print(f"\n  KEY INSIGHT: Ising-guided fuzzing concentrates test effort on")
    print(f"  boundary values (0, -1, empty list, max int) by encoding them")
    print(f"  as low-energy states. This is most effective for bugs that only")
    print(f"  manifest at specific boundary conditions (off-by-one, missing")
    print(f"  null checks, sign errors). For bugs that trigger on many inputs")
    print(f"  (wrong operator), random fuzzing may match or exceed Ising.")

    print(sep)
    return 0


if __name__ == "__main__":
    sys.exit(main())
