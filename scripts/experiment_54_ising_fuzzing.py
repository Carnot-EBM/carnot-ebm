#!/usr/bin/env python3
"""Experiment 54: Ising-guided fuzzing for differential testing.

**Researcher summary:**
    Uses the Ising energy landscape to GENERATE adversarial test inputs that
    are likely to expose bugs in LLM-generated code. Constraint violations
    map to energy terms so that low-energy samples correspond to edge-case
    inputs (boundary values, empty collections, sign flips). Differential
    testing compares a buggy function against a known-correct reference on
    each generated input. Compared against uniform random fuzzing.

**Detailed explanation for engineers:**
    Traditional random fuzzing generates test inputs uniformly at random.
    This misses bugs that only trigger on specific edge cases (e.g., empty
    list, zero, max-int, off-by-one boundaries). This experiment takes a
    different approach:

    1. **encode_input_space(func_signature, constraints)** — Maps each
       function parameter to a block of Ising spins. The spin configuration
       encodes a concrete value for that parameter. Constraints (type bounds,
       edge cases, known-dangerous values) become energy terms:
       - Edge-case values (0, -1, empty list, max int) get LOW bias,
         meaning the sampler is MORE likely to produce them.
       - Mid-range "boring" values get HIGH bias (less likely).
       - Constraint violations (e.g., index >= length) become coupling
         terms that ATTRACT the sampler toward boundary conditions.

    2. **ising_fuzz(func, reference_func, biases, J, n_samples)** — Uses
       ``ParallelIsingSampler`` to generate low-energy spin configurations,
       decodes each into concrete function arguments, and runs BOTH the
       buggy function and the correct reference on each input. Any
       disagreement in output is a detected bug.

    3. **Comparison** — The same number of test inputs are generated by
       (a) Ising-guided sampling and (b) uniform random. We measure:
       - Number of bugs found by each method
       - Unique bug signatures found
       - Time to first bug discovery

    4. **Test suite** — 8 function pairs covering common LLM code-generation
       bugs: off-by-one, missing null check, overflow, wrong operator,
       missing base case, type coercion, boundary error, sign error.

Usage:
    JAX_PLATFORMS=cpu .venv/bin/python scripts/experiment_54_ising_fuzzing.py

Spec: REQ-VERIFY-001, REQ-VERIFY-002, REQ-VERIFY-003, SCENARIO-VERIFY-003
"""

